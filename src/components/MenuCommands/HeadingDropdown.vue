<template>
  <el-dropdown
    placement="bottom"
    trigger="hover"
    @command="i => i > 0
      ? editorContext.commands.heading({ level: i })
      : editorContext.commands.paragraph()
    "
  >
    <command-button
      :is-active="isHeadingActive(undefined)"
      tooltip="heading"
      icon="heading"
    />
    <el-dropdown-menu slot="dropdown">
      <el-dropdown-item
        :command="0"
        :class="{
          'el-dropdown-menu__item--active': editorContext.isActive.paragraph(),
        }"
      >
        <span>{{ i18n('controls.paragraph') }}</span>
      </el-dropdown-item>
      <el-dropdown-item
        v-for="i in level"
        :key="i"
        :command="i"
        :class="{
          'el-dropdown-menu__item--active': isHeadingActive(i),
        }"
      >
        <component :is="'h' +i">
          {{ i18n('controls.heading') }} {{ i }}
        </component>
      </el-dropdown-item>
    </el-dropdown-menu>
  </el-dropdown>
</template>

<script>
import { isHeadingActive } from '../../utils/heading';
import CommandButton from './CommandButton.vue';
import i18nMixin from '../../mixins/i18nMixin';

export default {
  name: 'HeadingDropdown',

  mixins: [i18nMixin],

  components: {
    CommandButton,
  },

  props: {
    editorContext: {
      type: Object,
      required: true,
    },
  },

  computed: {
    editor () {
      return this.editorContext.editor;
    },

    level () {
      return this.editor.extensions.options.heading.level;
    },
  },

  methods: {
    isHeadingActive (level) {
      return isHeadingActive(this.editor.state, level);
    },
  },
};
</script>
